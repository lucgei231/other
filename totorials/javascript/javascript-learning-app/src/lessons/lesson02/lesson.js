// Lesson 2: Variables and Data Types

// This lesson covers the basics of variables and data types in JavaScript.

// Declaring variables
var name = "John"; // String
let age = 30; // Number
const isStudent = false; // Boolean

// Displaying variables in the console
console.log("Name:", name);
console.log("Age:", age);
console.log("Is Student:", isStudent);

// Example of changing a variable
age = 31; // Updating the value of age
console.log("Updated Age:", age);

// Data types demonstration
let number = 10; // Number
let text = "Hello, World!"; // String
let isTrue = true; // Boolean
let nothing = null; // Null
let notDefined; // Undefined

console.log("Number:", number);
console.log("Text:", text);
console.log("Boolean:", isTrue);
console.log("Null:", nothing);
console.log("Undefined:", notDefined);