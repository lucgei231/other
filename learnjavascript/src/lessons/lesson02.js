// Lesson 2: Variables and Data Types in JavaScript

// In this lesson, we will learn about variables and the different data types in JavaScript.
// We will cover how to declare variables using var, let, and const, and explore the various data types such as strings, numbers, booleans, null, undefined, and objects.

// Declaring variables
var name = "John"; // String
let age = 30; // Number
const isStudent = false; // Boolean

// Displaying variable values
console.log("Name:", name);
console.log("Age:", age);
console.log("Is Student:", isStudent);

// Data types
let myString = "Hello, World!"; // String
let myNumber = 42; // Number
let myBoolean = true; // Boolean
let myNull = null; // Null
let myUndefined; // Undefined
let myObject = { key: "value" }; // Object

// Displaying data types
console.log("String:", myString);
console.log("Number:", myNumber);
console.log("Boolean:", myBoolean);
console.log("Null:", myNull);
console.log("Undefined:", myUndefined);
console.log("Object:", myObject);