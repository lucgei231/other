// This is the JavaScript code for Lesson 01: Introduction to JavaScript

// 1. What is JavaScript?
// JavaScript is a programming language that allows you to implement complex features on web pages.

// 2. Basic Syntax
// JavaScript code can be written directly in HTML files or in separate .js files.

// Example of a simple JavaScript program
console.log("Hello, World!");

// 3. Variables
// Variables are used to store data values. You can declare a variable using var, let, or const.

let greeting = "Hello, JavaScript!";
console.log(greeting);

// 4. Data Types
// JavaScript has several data types including strings, numbers, booleans, objects, and arrays.

let number = 42; // Number
let isJavaScriptFun = true; // Boolean
let fruits = ["apple", "banana", "cherry"]; // Array
let person = { name: "John", age: 30 }; // Object

// 5. Functions
// Functions are blocks of code designed to perform a particular task.

function sayHello(name) {
    return `Hello, ${name}!`;
}

console.log(sayHello("Alice")); // Calling the function

// 6. Control Structures
// JavaScript uses control structures like if statements and loops to control the flow of the program.

if (number > 0) {
    console.log("The number is positive.");
} else {
    console.log("The number is not positive.");
}

